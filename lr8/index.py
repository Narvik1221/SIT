# Функция для нахождения начала содержательной части строки
def find_content_start(text):
    """
    Находит индекс первого не пробельного символа в строке.
    :param text: Строка, в которой осуществляется поиск
    :return: Индекс первого не пробельного символа
    """
    for i, char in enumerate(text):
        if not char.isspace():  # Проверяем, является ли символ НЕ пробелом
            return i
    return len(text)  # Если в строке только пробелы, возвращаем длину строки

# Функция для выделения содержательной части строки
def extract_content(text):
    """
    Извлекает содержательную часть строки от первого
    не пробельного символа до следующего пробельного символа.
    :param text: Строка, из которой извлекается содержательная часть
    :return: Содержательная часть строки
    """
    start_index = find_content_start(text)  # Находим начало содержательной части
    content = ""
    for char in text[start_index:]:
        if char.isspace():  # Останавливаемся, если встречаем пробел
            break
        content += char  # Добавляем символ в содержательную часть
    return content

# Функция для сравнения содержательных частей двух строк
def compare_contents(text1, text2):
    """
    Сравнивает содержательные части двух строк.
    :param text1: Первая строка
    :param text2: Вторая строка
    :return: Результат сравнения в виде строки
    """
    content1 = extract_content(text1)  # Извлекаем содержательную часть из первой строки
    content2 = extract_content(text2)  # Извлекаем содержательную часть из второй строки
    if content1 == content2:  # Сравниваем извлеченные части
        return "Содержательные части совпадают."
    else:
        return "Содержательные части не совпадают."

# Основная часть программы
if __name__ == "__main__":
    # Вводим две строки текста
    text1 = input("Введите первую строку: ")
    text2 = input("Введите вторую строку: ")

    # Сравниваем содержательные части и выводим результат
    result = compare_contents(text1, text2)
    print(result)
